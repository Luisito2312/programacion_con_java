/*
 Este código va de crear dos variables asignarles valores y hacer operaciones con ellas.
 * 
 * @author Luis Godino
 */
public class T02Ejercicio1 {
  public static void main(String[] args) {
    
    int x = 144;
    int y = 999;

    System.out.println("El valor de la variable x es: " + x + ". Y el valor de la variable y es: "+y);
    System.out.println("La suma de las varaibles es: " + (x + y));
    System.out.println("La resta de las variables es: " + (x - y));
    System.out.println("La multiplicación de las variables es: " + (x * y));
    System.out.println("La división de las variables es: " + ((double)x / y));
  }
}
